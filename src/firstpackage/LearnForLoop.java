package firstpackage;


public class LearnForLoop {
    public static void main(String[]args){
        //print numbers from 1 to 10
        for (int i =1; i<=10; i++)
            System.out.println(i);
        System.out.println("-------------------------------------------");

        // print odd numbers
        for(int a=1; a<=10; a+=2)
            System.out.println(a);
    }


}
